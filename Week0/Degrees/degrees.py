def trace_path(vertex):
    path = []
    while(vertex.parent != None):
        path.append((vertex.action, vertex.state))
        vertex = vertex.parent
    correct_path = []

    #Reverse path 
    for i in range(len(path)):
        correct_path.append(path[len(path) - i - 1])

    return correct_path   

